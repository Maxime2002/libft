#include"libft.h"

int	ft_atoi(const char *nptr)
{
	int	rep;
	int	i;
	int	sign;

	i = 0;
	rep = 0;
	sign = 1;
	while ((nptr[i] >= '\t' && nptr[i] <= '\r') || nptr[i] == ' ')
		i++;
	if (nptr[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		rep = rep * 10 + (nptr[i] - '0');
		i++;
	}
	return (rep * sign);
}
